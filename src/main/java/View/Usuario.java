    public void buscarUsuario(String usuario) throws SQLException{
        
        ConexaoMySQL sql = new ConexaoMySQL();  
        String[] user = sql.buscarUsuario(Consultas.BUSCA_USER, usuario);
        
        if(campoNome2.getText().length() < 2){
            message1.setText("Digite um nome de Usuário!!!");
            
        }else if(user != null){
            message1.setText("Usuário encontrado!!!");
            message1.setForeground(Color.green);
            campoIdUse.setText(user[0]);
            campoLogin.setText(user[1]);
            campoNome3.setText(user[2]);
            campoSenha.setText(user[3]);

            campoLogin.setEditable(false);
            campoNome3.setEditable(false);
            campoSenha.setEditable(false);
            
            campoLogin.setBackground(new Color(153,153,153));
            campoNome3.setBackground(new Color(153,153,153));
            campoSenha.setBackground(new Color(153,153,153));

            editar.setSelected(false);
        }else{
            message1.setText("Usuário não encontrado!!!");
            message1.setForeground(Color.red);
        }
        
    }
    private boolean excluirUsuario(int idUser) throws SQLException{
        ConexaoMySQL sql = new ConexaoMySQL();  
        boolean status = sql.removerUser(Consultas.REMOVE_USER, idUser);
        
        message.setText("Usuário cadastrado!!!");
        message.setForeground(new Color(0,204,51));
        limparCampos();
        
        return status;
    }
    
    private boolean editarUsuario(String idUser, String user, String nomeUser, String senhaUser) throws SQLException{
        ConexaoMySQL sql = new ConexaoMySQL();  
        boolean status = sql.editarUser(Consultas.EDITAR_USER, user, nomeUser, senhaUser, idUser);
        message.setText("Usuário modificado!!!");
        message.setForeground(new Color(0,204,51));
        return status;
    }
    
    private boolean cadastrarUsuario(String user, String nomeUser, String senhaUser) throws SQLException{
        ConexaoMySQL sql = new ConexaoMySQL();  
        boolean status = sql.cadastrarUser(Consultas.CADASTRO_USER, user, nomeUser, senhaUser);
        message.setText("Usuário cadastrado!!!");
        message.setForeground(new Color(0,204,51));
        return status;
    }
    private void limparCampos(){
        campoIdUse.setText("");
        campoLogin.setText("");
        campoNome3.setText("");
        campoSenha.setText("");
    }
    
    
