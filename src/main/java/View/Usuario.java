    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        message.setText("");
        message1.setText("");
            
            if(campoIdUse.getText().length() > 0 ){
               
                int idUser = Integer.parseInt(campoIdUse.getText());
            
            try {
                if(!excluirUsuario(idUser)){
                    
                    message.setText("Usuário excluido!!!");
                    message.setForeground(Color.green);
                }else{
                    message.setText("Error !!!");
                    message.setForeground(Color.red);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        message.setText("");
        message1.setText("");
        
        if(campoIdUse.getText().length() > 0){
            try {
                editarUsuario(campoIdUse.getText(), campoLogin.getText(), campoNome3.getText(), campoSenha.getText());
            } catch (SQLException ex) {
                Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            try {
                if(campoLogin.getText().length() > 0 && campoNome3.getText().length() > 0 && campoSenha.getText().length() > 0){
                    cadastrarUsuario(campoLogin.getText(), campoNome3.getText(), campoSenha.getText());
                }else{
                    message.setText("Verifique os dados!!!");
                }

            } catch (SQLException ex) {
                Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

