    public void listarLancamentoData() throws SQLException{
        
        ConexaoMySQL sql = new ConexaoMySQL(); 
        String busca[] = sql.BuscarLancamentoData(Consultas.BUSCA_LANCAMENTOS_SEM_DATA);

        DefaultListModel model = new DefaultListModel();

        model.clear();
        jList1.setModel(model);
        
        for (int i = 0; i < busca.length; i++) {
          
            model.addElement(busca[i]); 
            
            jList1.setModel(model);
        }
        
    }
    public void listarLancamento(String data) throws SQLException{

        ConexaoMySQL sql = new ConexaoMySQL();  
        
        String busca[][] = sql.BuscarLancamentoData(Consultas.BUSCA_LANCAMENTOS_DATA, data);
        
        Object[][] tabela = new Object[busca.length][3];
        
        for (int i = 0; i < busca.length; i++) {

            int totalEntrada = Integer.parseInt(busca[i][3]) + Integer.parseInt(busca[i][4]) +  Integer.parseInt(busca[i][5]) +  Integer.parseInt(busca[i][6]) +  Integer.parseInt(busca[i][8]); 
            int totalSaida =  Integer.parseInt(busca[i][8]); 
            int saldo = totalEntrada - totalSaida; 
            
            tabela[i][0] = NumberFormat.getCurrencyInstance().format(totalEntrada);
            tabela[i][1] = NumberFormat.getCurrencyInstance().format(totalSaida);
            tabela[i][2] = NumberFormat.getCurrencyInstance().format(saldo);

        }  
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            tabela,
            new String [] {
                "Total entradas", "Total saidas", "Saldo"
            }
        ));
    }
